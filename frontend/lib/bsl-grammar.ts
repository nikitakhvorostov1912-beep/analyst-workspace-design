/**
 * Prism.js grammar для BSL.
 * Экспортируется для регистрации: Prism.languages.bsl = bslGrammar
 */
// eslint-disable-next-line @typescript-eslint/no-explicit-any
export const bslGrammar: Record<string, any> = {
  // Комментарии: однострочные // и многострочные /* */
  comment: [
    { pattern: /\/\/.*$/m, greedy: true },
    { pattern: /\/\*[\s\S]*?\*\//, greedy: true },
  ],

  // Строки в двойных кавычках (BSL: "" escape внутри строки)
  string: {
    pattern: /"(?:""|[^"])*"/,
    greedy: true,
  },

  // Директивы компиляции: &НаКлиенте, &НаСервере, &НаСервереБезКонтекста
  directive: {
    pattern: /^\s*&[а-яёА-ЯЁa-zA-Z]+/m,
    alias: "keyword",
  },

  // Области: #Область, #КонецОбласти
  region: {
    pattern: /#(?:Область|КонецОбласти)(?:\s+\w+)?/,
    alias: "keyword",
  },

  // Ключевые слова BSL (используем lookahead/lookbehind для кириллицы)
  keyword: {
    pattern: wb(KEYWORDS.join("|")),
  },

  // Встроенные функции платформы
  builtin: {
    pattern: wb(BUILTINS.join("|")),
  },

  // Числа: целые и дробные
  number: {
    pattern: /\b\d+(?:\.\d+)?\b/,
  },

  // Пунктуация
  punctuation: {
    pattern: /[{}[\];().,]/,
  },
};
