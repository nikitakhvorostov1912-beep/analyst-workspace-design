/**
 * Prism.js grammar for BSL (Builtin Script Language) — 1С Enterprise.
 * Covers keywords, builtins, strings, numbers, comments, directives, regions.
 *
 * Примечание: \b не работает с кириллическими символами в JavaScript regex,
 * поэтому используем lookahead/lookbehind для границ слов.
 */

// Символы которые НЕ могут быть рядом с keyword/builtin
// (т.е. не буква/цифра/подчёркивание из ASCII + кириллицы)
const WB_BEFORE = "(?<![а-яёА-ЯЁa-zA-Z_\\d])";
const WB_AFTER = "(?![а-яёА-ЯЁa-zA-Z_\\d])";

function wb(pattern: string): RegExp {
  return new RegExp(`${WB_BEFORE}(?:${pattern})${WB_AFTER}`);
}

